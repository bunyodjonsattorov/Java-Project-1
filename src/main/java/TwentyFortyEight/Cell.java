package TwentyFortyEight;

public class Cell {
    private final int x;
    private final int y;

    public Cell(int x, int y) {
        this.x = x;  // Initialize x
        this.y = y;  // Initialize y
    }

    public int getX() {
        return x;  // Return x coordinate
    }

    public int getY() {
        return y;  // Return y coordinate
    }
}
